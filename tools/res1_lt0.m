P41=Ph(4,1);P42=Ph(4,2);P43=Ph(4,3);P44=Ph(4,4);

Vs(1,:) = 1/(a*(s - s1)^2*(s - s2)^2*(s - s3)^2)*...
  (-(2*B + s)*(s - s1)*(s - s2)*(s - s3)*...
    (2*B*((P42*Pf1 - P32*Pf2)*(L + 3*u) + K*(P42*Pf1 - P32*Pf2 + ...
         2*k*P44*Pf1*u - 2*k*P34*Pf2*u)) + ...
     2*s*(u*(3*P42*Pf1 - 3*P32*Pf2 + 2*k*(-P43*Pf1 + 2*P44*Pf1 + P33*Pf2 - ...
           2*P34*Pf2)*u) + L*(P42*Pf1 - P32*Pf2 + 2*k*(P44*Pf1 - P34*Pf2)*...
          u))) + (2*B + s)*(s - s1)*(s - s2)*(4*B^2*K*(P42*Pf1 - P32*Pf2) + ...
     2*B*s*((P42*Pf1 - P32*Pf2)*(L + 3*u) + ...
       K*(P42*Pf1 - P32*Pf2 + 2*k*P44*Pf1*u - 2*k*P34*Pf2*u)) + ...
     s^2*(u*(3*P42*Pf1 - 3*P32*Pf2 + 2*k*(-P43*Pf1 + 2*P44*Pf1 + P33*Pf2 - ...
           2*P34*Pf2)*u) + L*(P42*Pf1 - P32*Pf2 + 2*k*(P44*Pf1 - P34*Pf2)*...
          u))) + (2*B + s)*(s - s1)*(s - s3)*(4*B^2*K*(P42*Pf1 - P32*Pf2) + ...
     2*B*s*((P42*Pf1 - P32*Pf2)*(L + 3*u) + ...
       K*(P42*Pf1 - P32*Pf2 + 2*k*P44*Pf1*u - 2*k*P34*Pf2*u)) + ...
     s^2*(u*(3*P42*Pf1 - 3*P32*Pf2 + 2*k*(-P43*Pf1 + 2*P44*Pf1 + P33*Pf2 - ...
           2*P34*Pf2)*u) + L*(P42*Pf1 - P32*Pf2 + 2*k*(P44*Pf1 - P34*Pf2)*...
          u))) + (2*B + s)*(s - s2)*(s - s3)*(4*B^2*K*(P42*Pf1 - P32*Pf2) + ...
     2*B*s*((P42*Pf1 - P32*Pf2)*(L + 3*u) + ...
       K*(P42*Pf1 - P32*Pf2 + 2*k*P44*Pf1*u - 2*k*P34*Pf2*u)) + ...
     s^2*(u*(3*P42*Pf1 - 3*P32*Pf2 + 2*k*(-P43*Pf1 + 2*P44*Pf1 + P33*Pf2 - ...
           2*P34*Pf2)*u) + L*(P42*Pf1 - P32*Pf2 + 2*k*(P44*Pf1 - P34*Pf2)*...
          u))) - (s - s1)*(s - s2)*(s - s3)*(4*B^2*K*(P42*Pf1 - P32*Pf2) + ...
     2*B*s*((P42*Pf1 - P32*Pf2)*(L + 3*u) + ...
       K*(P42*Pf1 - P32*Pf2 + 2*k*P44*Pf1*u - 2*k*P34*Pf2*u)) + ...
     s^2*(u*(3*P42*Pf1 - 3*P32*Pf2 + 2*k*(-P43*Pf1 + 2*P44*Pf1 + P33*Pf2 - ...
     2*P34*Pf2)*u) + L*(P42*Pf1 - P32*Pf2 + 2*k*(P44*Pf1 - P34*Pf2)*u))));
  
  Vs(2,:)= 1/(a*(s - s1)^2*(s - s2)^2*(s - s3)^2)*...
  ((2*B + s)*(s - s1)*(s - s2)*(s - s3)*...
    (2*B*((P41*Pf1 - P31*Pf2)*(L + 3*u) + K*(P41*Pf1 - P31*Pf2 +... 
         2*k*P43*Pf1*u - 2*k*P33*Pf2*u)) + ...
     2*s*(L*(P41*Pf1 - P31*Pf2 + 2*k*(P43*Pf1 - P33*Pf2)*u) + ...
       u*(3*P41*Pf1 - 3*P31*Pf2 + 2*k*(2*P43*Pf1 - P44*Pf1 - 2*P33*Pf2 + ...
           P34*Pf2)*u))) - (2*B + s)*(s - s1)*(s - s2)*...
    (4*B^2*K*(P41*Pf1 - P31*Pf2) + 2*B*s*((P41*Pf1 - P31*Pf2)*(L + 3*u) + ...
       K*(P41*Pf1 - P31*Pf2 + 2*k*P43*Pf1*u - 2*k*P33*Pf2*u)) + ...
     s^2*(L*(P41*Pf1 - P31*Pf2 + 2*k*(P43*Pf1 - P33*Pf2)*u) + ...
     u*(3*P41*Pf1 - 3*P31*Pf2 + 2*k*(2*P43*Pf1 - P44*Pf1 - 2*P33*Pf2 + ...
     P34*Pf2)*u))) - (2*B + s)*(s - s1)*(s - s3)*...
    (4*B^2*K*(P41*Pf1 - P31*Pf2) + 2*B*s*((P41*Pf1 - P31*Pf2)*(L + 3*u) + ...
       K*(P41*Pf1 - P31*Pf2 + 2*k*P43*Pf1*u - 2*k*P33*Pf2*u)) + ...
     s^2*(L*(P41*Pf1 - P31*Pf2 + 2*k*(P43*Pf1 - P33*Pf2)*u) + ...
       u*(3*P41*Pf1 - 3*P31*Pf2 + 2*k*(2*P43*Pf1 - P44*Pf1 - 2*P33*Pf2 + ...
           P34*Pf2)*u))) - (2*B + s)*(s - s2)*(s - s3)*...
    (4*B^2*K*(P41*Pf1 - P31*Pf2) + 2*B*s*((P41*Pf1 - P31*Pf2)*(L + 3*u) + ...
       K*(P41*Pf1 - P31*Pf2 + 2*k*P43*Pf1*u - 2*k*P33*Pf2*u)) + ...
     s^2*(L*(P41*Pf1 - P31*Pf2 + 2*k*(P43*Pf1 - P33*Pf2)*u) + ...
       u*(3*P41*Pf1 - 3*P31*Pf2 + 2*k*(2*P43*Pf1 - P44*Pf1 - 2*P33*Pf2 + ...
           P34*Pf2)*u))) + (s - s1)*(s - s2)*(s - s3)*...
    (4*B^2*K*(P41*Pf1 - P31*Pf2) + 2*B*s*((P41*Pf1 - P31*Pf2)*(L + 3*u) + ...
       K*(P41*Pf1 - P31*Pf2 + 2*k*P43*Pf1*u - 2*k*P33*Pf2*u)) + ...
     s^2*(L*(P41*Pf1 - P31*Pf2 + 2*k*(P43*Pf1 - P33*Pf2)*u) + ...
       u*(3*P41*Pf1 - 3*P31*Pf2 + 2*k*(2*P43*Pf1 - P44*Pf1 - 2*P33*Pf2 + ...
       P34*Pf2)*u))));
    
Vs(3,:) = 1/(a*(s - s1)^2*(s - s2)^2*(s - s3)^2)*...
  2*k*u*(4*B^2*K*(P42*Pf1 - P32*Pf2)*(2*s^3 + s1*s2*s3 - s^2*(s1 + s2 + s3)) + ...
    s^2*(3*s1*s2*s3 + s^2*(s1 + s2 + s3) - 2*s*(s2*s3 + s1*(s2 + s3)))*...
     (u*(P41*Pf1 + 2*P42*Pf1 - P31*Pf2 - 2*P32*Pf2 + 2*k*P44*Pf1*u - ...
        2*k*P34*Pf2*u) + L*(P42*Pf1 - P32*Pf2 + 2*k*P44*Pf1*u - ...
        2*k*P34*Pf2*u)) + 2*B*s*(s^3 + 2*s1*s2*s3 - s*(s2*s3 + s1*(s2 + s3)))*...
     (L*(P42*Pf1 - P32*Pf2) + (P41*Pf1 + 2*P42*Pf1 - (P31 + 2*P32)*Pf2)*u + ...
     K*(P42*Pf1 - P32*Pf2 + 2*k*(P44*Pf1 - P34*Pf2)*u)));
  
  Vs(4,:) = -(1/(a*(s - s1)^2*(s - s2)^2*(s - s3)^2)*...
   2*k*u*(4*B^2*K*(P41*Pf1 - P31*Pf2)*(2*s^3 + s1*s2*s3 - s^2*(s1 + s2 + s3)) + ...
     s^2*(3*s1*s2*s3 + s^2*(s1 + s2 + s3) - 2*s*(s2*s3 + s1*(s2 + s3)))*...
      (L*(P41*Pf1 - P31*Pf2 + 2*k*P43*Pf1*u - 2*k*P33*Pf2*u) + ...
       u*(2*P41*Pf1 + P42*Pf1 - 2*P31*Pf2 - P32*Pf2 + 2*k*P43*Pf1*u - ...
         2*k*P33*Pf2*u)) + 2*B*s*(s^3 + 2*s1*s2*s3 - s*(s2*s3 + s1*(s2 + s3)))*...
      (L*(P41*Pf1 - P31*Pf2) + (2*P41*Pf1 + P42*Pf1 - (2*P31 + P32)*Pf2)*u + ...
       K*(P41*Pf1 - P31*Pf2 + 2*k*(P43*Pf1 - P33*Pf2)*u))));